class User:
    id: str
    username: str
    email: str
    password_hash: str
    salt: str
    created_at: str
    last_login: str = None
    is_active: bool = True
    empire_id: str = None

class SupabaseAuthDatabase:
    def __init__(self):
        self.client = get_supabase_client()
        self.service_client = get_supabase_service_client()  # For admin operations
        self.init_auth_tables()
    
